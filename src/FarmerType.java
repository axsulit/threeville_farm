/**
 * Classification of a Farmer.
 */
public enum FarmerType {
    /**
     * A regular, ordinary farmer.
     */
    Farmer,
    /**
     * A farmer that has registered once.
     */
    Registered_Farmer,
    /**
     * A farmer that has registered twice.
     */
    Distinguished_Farmer,
    /**
     * A farmer that has registered thrice.
     */
    Legendary_Farmer;
}
